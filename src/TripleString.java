public class TripleString {
    private String stringOne;
    private String stringTwo;
    private String stringThree;

    public TripleString(String one, String two, String three) {
        stringOne = one;
        stringTwo = two;
        stringThree = three;
    }

    public void setStringOne(String value) {
        stringOne = value;
    }

    public String getStringOne() {
        return stringOne;
    }

    public void setStringTwo(String value) {
        stringTwo = value;
    }

    public String getStringTwo() {
        return stringTwo;
    }

    public void setStringThree(String value) {
        stringThree = value;
    }

    public String getStringThree() {
        return stringThree;
    }
}
